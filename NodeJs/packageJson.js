/*
1. What is package.json ?
Ans. Package.json file is a metadata file that contains information about a node js 
    project and its dependencies.

    Some key aspects of the package.json file:
    1> Project information
    2> Dependencies
    3> Development dependencies (Dev dependencies)
    4> Scripts
    5> Version control and collabration


2. What is dev-dependency ?
Ans. Development dependency refers to a package or library that is required during
    the development processes but not during the production runtime of the application
    or project.


3. Difference between dependency and dev-dependency ?
Ans. Regular dependencies are necessary for the project to function correctly in a
    production environment, while dev-dependencies are use for development specific
    tasks and are not needed for the project runtime environment.
*/